    private void btnPop_pilaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPop_pilaActionPerformed
        // TODO add your handling code here:
        int numero = pila.pop();
        if(numero != -1)
        {
            JOptionPane.showMessageDialog(this,"Numero: "+numero,"Pila", JOptionPane.INFORMATION_MESSAGE);
            txtLog.append("Se ha obtenido el numero "+numero +" de pila.\n");
        }
        else
        {
            JOptionPane.showMessageDialog(this,"Pila vacia","pila", JOptionPane.INFORMATION_MESSAGE);
            txtLog.append("Pila vacia.\n");
        }
    }//GEN-LAST:event_btnPop_pilaActionPerformed

