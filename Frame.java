public void escribir(Color c, String s) { // better implementation--uses StyleContext
    StyleContext sc = StyleContext.getDefaultStyleContext();
  
    int len = s.length(); // same value as getText().length();
    txtLog.setCaretPosition(len);  // place caret at the end (with no selection)
    txtLog.setCaretColor(c);
    txtLog.replaceSelection(s); // there is no selection, so inserts at caret
  }
